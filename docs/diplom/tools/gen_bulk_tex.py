def paragraph(idx: int) -> str:
    topic = TOPICS[idx % len(TOPICS)]
    o = random.choice(OPENERS)
    m = random.choice(MID)
    c = random.choice(CLOSERS)
    extra = (
        f" Рассмотрим тему «{topic}» в приложении к программной системе "
        f"оркестрации Chronos (фрагмент {idx + 1}). "
        f"{o} {m} {c}"
    )
    return extra


def main() -> None:
    OUT.parent.mkdir(parents=True, exist_ok=True)
    lines: list[str] = []
    lines.append("% Сгенерировано скриптом tools/gen_bulk_tex.py (структурированный связный текст по тематике Chronos).")
